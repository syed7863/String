package syed;

public class EmptyRepresentation {

	public static void main(String[] args) {
		
		
		String s= new String();
		System.out.println(s);
		
		
		
		
		String s1= new String("syed");
		System.out.println(s1);
		
		char[] c= {'a','e','i','o','u'};
		String s2= new String(c);
		System.out.println(s2);
	}

}
